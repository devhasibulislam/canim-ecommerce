/* internal import */
const brandService = require("../services/brand.service");

/* add new brand */
exports.addBrand = async (req, res, next) => {
  try {
    await brandService.addBrand(req, res);
  } catch (error) {
    next(error);
  } finally {
    console.log(`Route: ${req.url} || Method: ${req.method}`);
  }
};

